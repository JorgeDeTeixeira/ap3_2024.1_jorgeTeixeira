package unidade1.src.gabarito_aula2;

public class LimiteInt {
    public static void main(String[] args) {
        int valorMaximo = Integer.MAX_VALUE;
        System.out.println("Valor máximo de um int: " + valorMaximo);

        int valorMinimo = Integer.MIN_VALUE;
        System.out.println("Valor mínimo de um int: " + valorMinimo);

        int estouroMaximo = valorMaximo + 1;
        System.out.println("Valor máximo de um int: " + estouroMaximo);
    }
}
